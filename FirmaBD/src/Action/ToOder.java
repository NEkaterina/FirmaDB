    private void comBox() {

        list = new ArrayList<>();
        list1 = new ArrayList<>();
        list2 = new ArrayList<>();

        try {
            Statement statement = c.createStatement();
            ResultSet rs = statement.executeQuery("select id_dp, surname, firstname,"
                    + " otchestvo, passport,birthday,address,phone from co_worker left join "
                    + "data_person on data_person.id_dp = co_worker.fk_dp_cw");
            while (rs.next()) {
                DataPerson item = new DataPerson(rs.getInt("id_dp"), rs.getString("surname"),
                        rs.getString("firstname"), rs.getString("otchestvo"),
                        rs.getString("passport"), rs.getDate("birthday"), rs.getString("address"),
                        rs.getString("phone"));

                list.add(item);
            }
            worker.setModel(new DefaultComboBoxModel(list.toArray()));

            rs = statement.executeQuery("select id_dp, surname, firstname,"
                    + " otchestvo, passport,birthday,address,phone from client left join "
                    + " data_person on data_person.id_dp = client.fk_dp_client");
            while (rs.next()) {
                DataPerson item = new DataPerson(rs.getInt("id_dp"), rs.getString("surname"),
                        rs.getString("firstname"), rs.getString("otchestvo"),
                        rs.getString("passport"), rs.getDate("birthday"), rs.getString("address"),
                        rs.getString("phone"));
                list2.add(item);
            }
            client.setModel(new DefaultComboBoxModel(list2.toArray()));

            rs = statement.executeQuery("SELECT *   FROM materials ");
            while (rs.next()) {
                System.out.println("jhg");
                Materials item = new Materials(rs.getInt("id_materials"), rs.getString("name_materials"),
                        rs.getInt("amount_materials"), rs.getInt("price_materials"),
                        rs.getInt("fk_providers"));
                list1.add(item);
            }
            material.setModel(new DefaultComboBoxModel(list1.toArray()));
        } catch (SQLException ex) {
            Logger.getLogger(ToOder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

