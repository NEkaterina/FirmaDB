        try {
            // TODO add your handling code here:
            Statement statement = c.createStatement();
            ResultSet rs = statement.executeQuery("select co_worker.id_cw as id from data_person,co_worker where data_person.id_dp=co_worker.fk_dp_cw and data_person.id_dp = "
                    + ((DataPerson) worker.getSelectedItem()).getId());
            int id_worker = 0;
            while (rs.next()) {
                id_worker = rs.getInt("id");
            }

            rs = statement.executeQuery("select client.id_client as id from data_person,client where data_person.id_dp=client.fk_dp_client and data_person.id_dp = "
                    + ((DataPerson) client.getSelectedItem()).getId());
            int id_client = 0;
            while (rs.next()) {
                id_client = rs.getInt("id");
            }
            statement.executeUpdate("insert into selling(number_contract,date_selling,fk_id_cw,fk_id_client) values("
                    + num.getText() + ",'" + date.getText() + "'," + id_worker + "," + id_client + ");");

            rs = statement.executeQuery("select  max(id_selling) as max from selling;");
            int id_sell = 0;
            while (rs.next()) {
                id_sell = rs.getInt("max");
            }
            statement.executeUpdate("insert into selling_materials(fk_id_selling,fk_id_materials,amount) values("
                    + id_sell + "," + ((Materials) material.getSelectedItem()).getId() + "," + amount.getText() + ");");

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage());

            return;
        }
        dispose();
