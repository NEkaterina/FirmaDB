    private void entryMaterials() {
        list = new ArrayList<>();
        try {
            Statement statement = c.createStatement();
            ResultSet rs = statement.executeQuery("select id_providers, company_name,address_company,"
                    + " phone_company from providers");
            while (rs.next()) {
                Providers tmp = new Providers(rs.getInt("id_providers"), rs.getString("company_name"),
                        rs.getString("address_company"), rs.getString("phone_company"));

                list.add(tmp);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddData.class.getName()).log(Level.SEVERE, null, ex);
        }
        jComboBox1.setModel(new DefaultComboBoxModel(getList().toArray()));
    }

