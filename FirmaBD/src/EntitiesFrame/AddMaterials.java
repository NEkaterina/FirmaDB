    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!check()) {
            return;
        }
        try {
            // TODO add your handling code here:
            Statement statement = getC().createStatement();
            if (getEditM() == null) {
                statement.executeUpdate("insert into materials (name_materials,"
                        + "amount_materials, price_materials,"
                        + "fk_providers) values ('" + jTextField1.getText() + "','" + jTextField2.getText()
                        + "','" + jTextField3.getText()
                        + "','" + ((Providers) jComboBox1.getSelectedItem()).getId() + "');");
            } else {
                statement.executeUpdate("update materials set  name_materials='" + jTextField1.getText()
                        + "',amount_materials='" + jTextField2.getText() + "',price_materials='" + jTextField3.getText()
                        + "',fk_providers='" + ((Providers) jComboBox1.getSelectedItem()).getId()
                        + "' where id_materials=" + getEditM().getId() + ";");
            }

        } catch (SQLException ex) {
            if (ex.getMessage().contains("User_Login_key")) {
                JOptionPane.showMessageDialog(new JFrame(), "This login already exist");
            } else {
                JOptionPane.showMessageDialog(new JFrame(), ex.getMessage());
            }
            return;
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

